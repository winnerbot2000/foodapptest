import SwiftUI

/// Defines a spacing scale used across the Food Journal app.  Using
/// consistent spacing improves visual rhythm and layout cohesion.
struct AppSpacing {
    /// Extra small spacing (4pt).
    static let xs: CGFloat = 4
    /// Small spacing (8pt).
    static let sm: CGFloat = 8
    /// Medium spacing (16pt).
    static let md: CGFloat = 16
    /// Large spacing (24pt).
    static let lg: CGFloat = 24
    /// Extra large spacing (32pt).
    static let xl: CGFloat = 32
}